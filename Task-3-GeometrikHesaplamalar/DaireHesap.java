public class DaireHesap {
    public  static void main(String[] args) {
        double r = 5.5;
        double pi = 3.1415;
        double alan = pi * r * r;
        double çevre = 2 * pi * r;
        System.out.println("pi = " + pi);
        System.out.println("çevre = " + çevre);
        System.out.println("alan = " + alan);
    }
}
