public class MilKilometreTablosu {
    public static void main(String[] args) {
        System.out.println("Dönüşüm Tablosu: Mil → Kilometre (1 mil = 1.609 km)");
        System.out.println("-----------------------------------------");
        System.out.println("Mil  Kilometre");

        System.out.println("1     " + (1 * 1.609));
        System.out.println("5     " + (5 * 1.609));
        System.out.println("10    " + (10 * 1.609));
        System.out.println("20    " + (20 * 1.609));
        System.out.println("50    " + (50 * 1.609));
    }
}
